    private void bt_simpanMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_bt_simpanMouseClicked
        // TODO add your handling code here:
        String nama = et_nama.getText();
        String username = et_username.getText();
        String password = et_password.getText();
        String passwordKonfir = et_password_konfir.getText();
        String cbRole = cb_role.getItemAt(cb_role.getSelectedIndex());
        
        if(!nama.isEmpty() && !username.isEmpty() && !password.isEmpty() && !passwordKonfir.isEmpty() && !cbRole.equals("Pilih")){
            if(password.equals(passwordKonfir)){
                if(cbRole.equals("Admin")){
                    cbRole = "0";
                }else if(cbRole.equals("User")){
                    cbRole = "1";
                }else if(cbRole.equals("Manager")){
                    cbRole = "2";
                }
                if(modelGlobal != null){
                    daoImpl.update(nama, username, password, cbRole, modelGlobal.getId());
                }else {
                    daoImpl.insert(nama, username, password, cbRole);
                }
            }else{
                JOptionPane.showMessageDialog(null,"Password Tidak sama !");
            }
        } else {
            JOptionPane.showMessageDialog(null,"Tidak Boleh Ada Yang Kosong !");
        }
        tampilData();
        clearData();
    }//GEN-LAST:event_bt_simpanMouseClicked

    private void bt_deleteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_bt_deleteMouseClicked
        // TODO add your handling code here:
        if(modelGlobal != null){
            daoImpl.delete(String.valueOf(modelGlobal.getId()));
            tampilData();
            clearData();
        }
    }//GEN-LAST:event_bt_deleteMouseClicked

