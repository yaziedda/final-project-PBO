    private void tampilData() {
        String[] kolom = {"ID","Nama","Username/NIK", "Role"};
        dtm = new DefaultTableModel(null, kolom);
        for(UserModel model : daoImpl.getList()){
            String[] row = new String[4];
            row[0] = String.valueOf(model.getId());
            row[1] = model.getNama();
            row[2] = String.valueOf(model.getUsername());
            String cbRole = model.getRole();
            if(cbRole.equals("0")){
                    cbRole = "Admin";
                }else if(cbRole.equals("1")){
                    cbRole = "User";
                }else if(cbRole.equals("2")){
                    cbRole = "Manager";
                }
            row[3] = cbRole;
            dtm.addRow(row);
        }
        tbl_main.setModel(dtm);
        tbl_main.clearSelection();
    }

